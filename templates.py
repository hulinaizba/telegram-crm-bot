def get_template(stage_key: str, style: str = "full") -> str:
    """Возвращает шаблон для указанного этапа"""
    stage_templates = TEMPLATES.get(stage_key, TEMPLATES["материалы"])
    return stage_templates.get(style, stage_templates["full"])


def format_template(stage_key: str, style: str = "full", **kwargs) -> str:
    """Форматирует шаблон с подстановкой переменных (name, deposit и т.д.)"""
    template = get_template(stage_key, style)
    try:
        return template.format(**kwargs)
    except (KeyError, ValueError):
        return template  # возвращаем как есть, если переменных нет


